char	*ft_strchr(const char *s, int c)
{
	char	*str;
	int		i;

	i = 0;
	str = (char *)s;
	c = (char)c;
	while (str[i] != '\0' && str[i] != c)
	{
		i++;
	}
	if (str[i] != c)
		return (0);
	else
		return (str + i);
}
